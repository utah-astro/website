                                                                         emitting supernova remnants, pulsar&nbsp;
                                        <span>wind nebulae, and active galactic nuclei that accelerate particles to speeds near
                                                                            the speed of light. These particles emit very-high-energy gamma-rays as they interact
                                                                            with the surrounding medium. Using HAWC and VERITAS gamma-ray observatories, he measures
                                                                            these gamma-rays and investigate questions such as, how these particles are moving
                                                                            away from the central engine, what fraction of the particles reach earth, and what
                                                                            is the nature of the medium between Earth and these objects.</span>
